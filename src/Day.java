public class Day {
	private String date;

	/**
	 * Constructor.
	 * @param date
	 */
	public Day(String date) {
		this.date = date;
	}

	/**
	 * Get the date.
	 * 
	 * @return the date
	 */
	public String getDate() {
		return date;
	}

	/**
	 * Set the date.
	 * 
	 * @param date
	 *            the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
}
